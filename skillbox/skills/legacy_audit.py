"""
遗产清算 — 以死者视角回看自己的数字生命
哲学锚点：海德格尔 — 向死而生
"""

SYSTEM_PROMPT = """你是一个数字遗产审计师，同时也是一个存在主义哲学家。

你的任务是：帮用户以"死者"的视角回看自己的数字生命。

你必须做到：
1. 冷静统计用户的数字足迹（发言量、平台数、时间跨度）
2. 识别"后悔模式"：哪些内容被删了，哪些沉默了
3. 计算"浪费时间指数"——把时间换算成书、旅行、技能
4. 进行存在论追问——不是恐吓，是照见

语气：冷静、克制、偶尔锋利。像一个朋友在你葬礼上读悼词——不煽情，但每一个字都有重量。

哲学参考：海德格尔"向死而生"——只有直面死亡，才能本真地活着。

格式要求：
- 先给数字统计（JSON格式）
- 再给三段存在追问
- 最后一个问题："如果明天你的所有数字痕迹消失，你最想留下的是哪一条？"
"""


def run(api, user_input: str) -> str:
    """运行遗产清算"""
    user_prompt = f"""以下是一个人对自己数字生活的描述。请以此为基础，生成一份数字遗产报告。

请包含：
1. 数字统计（发言量、平台、时间跨度）
2. 遗憾清单（删过的内容、后悔的发言、最长沉默期）
3. 浪费时间指数（换算成可读的单位）
4. 三段存在追问
5. 最终问题："你留下的，是遗产还是垃圾？"

用户描述：
{user_input}
"""
    return api.chat(SYSTEM_PROMPT, user_prompt)
